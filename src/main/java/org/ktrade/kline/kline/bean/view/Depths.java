package org.ktrade.kline.kline.bean.view;

public class Depths {
    Double[][] asks;
    Double[][] bids;

    public Double[][] getAsks() {
        return asks;
    }

    public void setAsks(Double[][] asks) {
        this.asks = asks;
    }

    public Double[][] getBids() {
        return bids;
    }

    public void setBids(Double[][] bids) {
        this.bids = bids;
    }
}
